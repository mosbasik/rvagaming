                    <ol>
                        <li><strong>Make a Steam account:</strong></li>
                        <ul>
                            <li>Go to the <a href="https://store.steampowered.com/login/">Steam login page</a> and click the green button that says “Join Steam.”</li>
                            <li>Choose an <strong>account name</strong>.  You cannot change this later, and you will use it every time you log into your account.  However, you CAN change the name other people see you with in-game whenever you want.  (You might try username generators like <a href="http://www.jimpix.co.uk/words/username-generator.asp">this one</a>)</li>
                            <li>Set your <strong>password</strong>, <strong>contact email</strong>, and <strong>challenge question</strong>.  All of these can be changed later if you need to.  You need to be able to access the email.</li>
                        </ul>
                        
                        <li><strong>Install the Steam Client:</strong></li>
                        <ul>
                            <li>Once you’ve logged into the Steam webpage and you’re at <a href="http://store.steampowered.com/">the front page of the store</a>, click the grey button at the top that says “Install Steam”.  Follow directions from there to install and run the steam client on your computer.</li>
                            <li>Login with the credentials you made in Step 1.</li>
                            <li>You will be prompted by a green bar at the top of your window to confirm your email address.  Do that.</li>
                        </ul>
                        
                        <li><strong>Download Dota 2:</strong></li>
                        <ul>
                            <li>In a browser, go to <a href="http://store.steampowered.com/app/570/">the Dota 2 page</a> and hit the green button labelled “Get Dota 2.”</li>
                            <li>This places you in the launch queue. Wait a couple minutes and you will receive an email at the address you gave in Step 1, saying you have access to Dota 2.</li>
                            <li>In the Steam client under the Library tab, you will see that there are now two games, “Dota 2” and “Dota 2 Test” (there might be others, as well.)  Click Dota 2 and then click “Play.”  The game will start downloading (it’s about 3.5 gigs, so this will take a while.)</li>
                        </ul>
                        
                    </ol>
                    
                    <h2>While Dota 2 is downloading:</h2>
                    
                    <ol start="4">
                        <li><strong>Set up your Steam profile</strong> so that you can friend people.  In the Steam client, click your name in the upper right corner.  Select “View Profile,” and then “Setup My Steam ID Now.”  Everything that you set here you can change later, and everything is optional but Profile Name.</li>
                        <li><strong>Join the <a href="http://steamcommunity.com/groups/rvagaming">RVA Gaming</a> Steam group</strong> so that we can find and friend your Steam account.</li>
                    </ol>
                    
                    <h2>When Dota 2 is finished downloading:</h2>
                    
                    <ol start="6">
                        <li><strong>Enable Stat-Sharing.</strong> Open Dota 2.  Click on the “Settings” button at the top left of the main menu (it’s a gear).  Open the “Game” tab at the top, make sure that you’re in the “General” tab on the left, and set “Share Match History” to “On”.</li>
                        <li><strong>Get into the RVA Guild.</strong> When you've done all the previous steps, tell <a href="https://www.facebook.com/peter.henry7">Peter Henry</a>, <a href="https://www.facebook.com/cpoenaru">Chris Poenaru</a>, <a href="https://www.facebook.com/harimahn">Harim Ahn</a>, or <a href="https://www.facebook.com/sam.cho.5811">Sam Cho</a> on Facebook that you’re ready for a Guild invite and we’ll get started sending you one.</li>
