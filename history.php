                    <h2>Welcome!</h2>
                    
                    <p>This started as the 2011 Mbega seniors playing Warcraft 3 DotA in their rooms.  When we graduated and all went to college, <a href="https://www.facebook.com/cpoenaru">Chris Poenaru</a>, <a href="https://www.facebook.com/sam.cho.5811">Sam Cho</a>, <a href="https://www.facebook.com/davidkyungho">David Park</a> and <a href="https://www.facebook.com/harimahn">Harim Ahn</a> kept in touch and played some matches every now and then over Garena.</p>
                    
                    <p>Dota 2 was released as an invite-only beta in 2011.  Chris was the first to get beta key, in early 2012.  Soon after, so did <a href="https://www.facebook.com/peter.henry7">Peter Henry</a>, who'd never played DotA before.  Peter and Harim farmed up twenty or so beta keys, which were given to Sam, David, and then to all the other guys who had played back at RVA, but who hadn't kept in touch - <a href="https://www.facebook.com/jsmanu92">Josh Manu</a>, <a href="https://www.facebook.com/lance.nordmeyer">Lance Nordemeyer</a>, <a href="https://www.facebook.com/jacob.gaudaur">Jacob Gaudaur</a>, <a href="https://www.facebook.com/timthebomb">Tim Nykamp</a>, and <a href="https://www.facebook.com/eunho.kim.167">Eunho Kim</a>.</p>
                    
                    <p>We still had some beta keys left over when the Class of 2012 graduated.  Harim lived pretty close to where <a href="https://www.facebook.com/junhaeng.lee.37853">Junhaeng Lee</a> and <a href="https://www.facebook.com/peterparkjunior">Peter Park</a> were in Korea, so he gave them keys.  Several months later, they brought in <a href="https://www.facebook.com/jlim1294">Yechan Lim</a> and <a href="https://www.facebook.com/carl.otuke">Mark Otuke</a>.</p>
                    
