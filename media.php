                            <li><a href="http://www.dota2lounge.com/">Dota 2 Lounge</a></br>This site allows you to see what cosmetics other players are willing to trade and hooks you up with matches.  Probably the most-used site of its kind.  Be prepared for haggling.</li>
                            <li><a href="http://www.dota2wh.com/">Dota 2 Warehouse</a></br>If you don't want to argue over trades, this site allows you to trade cosmetics without directly contacting other players by using an escrow system.  You "sell" items and gold is credited to your account.  You then "buy" other items with the gold.</li>
