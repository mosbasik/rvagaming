                    <h2>Reference Material</h2>
                        <ul>
                            <li><a href="http://dota2.gamepedia.com/Dota_2_Wiki">The Dota 2 Wiki</a></br> The more-or-less official wiki for the game.  Has a lot of good info and updates quickly after patches.</li>
                            <li><a href="http://www.youtube.com/playlist?list=PL86E04995E07F6BA8">DotaCinema’s Hero Spotlights</a></br> A YouTube playlist with very short (~2 min) videoes summarizing each hero in the game.  If you want to see a hero in action for a better idea of what they do than reading a list of stats can convey, check here.</li>
                            <li><a href="http://critwhale.com/DotaMap.jpg">CritWhale’s Dota Map</a></br> A top-down view of the Dota 2 map displaying all juke paths (including the ones you have to cut trees to use), secret buying places, and neutral spawn boxes.  The creator keeps it updated at this link.</li>
                            <li><a href="http://steamcommunity.com/sharedfiles/filedetails/?id=125602574">A Detailed Guide to the Mechanics of Dota 2</a></br> A very in-depth, text and numbers based guide.  Intended for intermediate to advanced players, I have it bookmarked as “The Dota 2 Bible.”  Check here for questions like “Can I disjoint Sven’s Storm Hammer by going invis with Shadow Blade?”  (You can’t, but you can disjoint Gyrocopter’s Homing Missile with Shadow Blade.)</li>
                            <li><a href="http://wiki.gosugamers.net/dota2/History_of_DotA">The GosuGamers History of DotA</a></br>A six-part detailed breakdown of the history of DotA, beginning with the 2001 Starcraft map "Aeon of Strife" and going up to version 6.48 in 2009.
