                    <p>Playing Dota online is not like playing it on LAN with your friends.  Even if you have plenty of experience on DotA, you should look through some of these guides as a refresher.  There are many tips and tricks that you will find useful, especially when coming from an environment that might consider Chen to be the suckiest hero in the game, or Invoker to be the hardest character to learn (neither of those are true).</p>
                    
                    <h2>Guides</h2>
                        <ul>
                            <li><a href="http://www.purgegamers.com/welcome-to-dota-you-suck">Purge’s Welcome to Dota You Suck</a></br> The original and best introduction to playing Dota online.  If you don’t read anything else, read this.  It’s long, but worth it.</li>
                            <li><a href="http://www.youtube.com/playlist?list=PLF50cvv4MhVrEdrQw2kGnJ7ag7eT1RfO2">DotaCinema’s Learn Dota 2</a></br> A Youtube playlist with 18 fairly detailed videos about a range of topics.  They range from basic things like gold and starting items to advanced mechanics like neutral stacking/pulling, animation cancelling, and advanced warding.</li>
                            <li><a href="https://www.youtube.com/results?q=purge+plays">Purge Plays</a></br> A large set of Youtube videos.  Each one is game played by Purge using a specific hero, and the whole game he gives commentary on why he is making certain decisions, why he's buying certain items, etc.  Very helpful when learning a new hero.</br></li>
